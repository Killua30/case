CREATE OR REPLACE FUNCTION get_all_test_results(emp_id INT)
RETURNS TABLE (
    TypeTestName VARCHAR(60),
    TestDate TIMESTAMP,
    TestResult DECIMAL(3, 0)
) AS $$
BEGIN
    RETURN QUERY
    SELECT 
        tt.TypeTestName,
        r.Date AS TestDate,
        r.TestResult
    FROM 
        Results r
    JOIN 
        TypeTest tt ON r.TypeTestId = tt.TypeTestId
    WHERE 
        r.EmpID = emp_id
    ORDER BY 
        r.Date DESC; 
END;
$$ LANGUAGE plpgsql;

