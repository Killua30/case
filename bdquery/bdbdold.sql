(
	EmpID INT PRIMARY KEY GENERATED ALWAYS AS IDENTITY,
	EmpSurname VARCHAR(60) NOT NULL CHECK (EmpSurname ~ '[А-Я][а-я]+'),
	EmpName VARCHAR(60) NOT NULL CHECK (EmpName ~ '[А-Я][а-я]+'),
	EmpPatronymic VARCHAR(60) CHECK (EmpPatronymic ~ '[А-Я][а-я]+' OR (EmpPatronymic  ISNULL)),
	EmpPosition VARCHAR(60) NOT NULL,
	EmpMail VARCHAR(60) NOT NULL UNIQUE CHECK (EmpMail ~ '^[^@]+@[^@]+\.[^@]+$'),
	EmpPassword VARCHAR(60) NOT NULL
);

