CREATE OR REPLACE FUNCTION get_last_test_results(emp_id INT)
RETURNS TABLE (
    TypeTestId INT,
    TypeTestName VARCHAR(60),
	SoftTrue boolean,
    TestDate TIMESTAMP,
    TestResult DECIMAL(3, 0)
) AS $$
BEGIN
    RETURN QUERY
    SELECT 
        tt.TypeTestId,
        tt.TypeTestComp,
		tt.SoftTrue,
        r.Date AS TestDate,
        r.TestResult
    FROM 
        Results r
    JOIN 
        TypeTest tt ON r.TypeTestId = tt.TypeTestId
    WHERE 
        r.EmpID = emp_id
        AND r.Date = (
            SELECT MAX(r2.Date)
            FROM Results r2
            WHERE r2.EmpID = emp_id AND r2.TypeTestId = r.TypeTestId
        )
	ORDER BY TypeTestId;
END;
$$ LANGUAGE plpgsql;

