AS
SELECT 
    e.EmpId, 
    e.EmpSurname, 
    e.EmpName, 
    e.EmpPatronymic, 
    e.EmpPosition,
    ROUND(AVG(CASE WHEN tt.SoftTrue = true THEN r.TestResult END), 2) AS AvSoftResult,
    ROUND(AVG(CASE WHEN tt.SoftTrue = false THEN r.TestResult END), 2) AS AvHardResult
FROM 
    Employee e
