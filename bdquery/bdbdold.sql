CREATE OR REPLACE FUNCTION make_test(test INT)
RETURNS TABLE (
  TestID INT,
  Question TEXT,
  VarA TEXT,
  VarB TEXT,
  VarC TEXT,
  VarD TEXT,
  Answer Varchar(1)
) AS $$
BEGIN
    RETURN QUERY
  SELECT * FROM Question 
  WHERE Question.TypeTestID = test
  ORDER BY RANDOM() 
  LIMIT 10;
END;
$$ LANGUAGE plpgsql;

